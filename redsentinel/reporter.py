def render_report(target, subdomains=None, ports=None, http_checks=None):
    """Render HTML report"""
    tpl = Template(HTML_TMPL)
    return tpl.render(
        target=target, 
        subdomains=subdomains or [], 
        ports=ports or {}, 
        http=http_checks or [], 
        generated=now_iso()
    )


def render_json_report(target, data):
    """Render JSON report"""
    report = {
        "target": target,
        "generated": now_iso(),
        "data": data
    }
    return json.dumps(report, indent=2)


def render_markdown_report(target, subdomains=None, ports=None, http_checks=None):
    """Render Markdown report"""
    md = f"""# RedSentinel Security Report

**Target:** {target}  
**Generated:** {now_iso()}

## Summary

- Subdomains found: **{len(subdomains) if subdomains else 0}**
- Open ports: **{len(ports) if ports else 0}**
- HTTP checks: **{len(http_checks) if http_checks else 0}**

## Subdomains

"""
    if subdomains:
        for sub in subdomains[:100]:
            md += f"- {sub}\n"
        if len(subdomains) > 100:
            md += f"- ... and {len(subdomains) - 100} more\n"
    
    md += "\n## Open Ports\n\n"
    if ports:
        md += "| Port | Status |\n"
        md += "|------|--------|\n"
        for p, o in ports.items():
            md += f"| {p} | {'ğŸŸ¢ Open' if o else 'ğŸ”´ Closed'} |\n"
    
    md += "\n## HTTP Checks\n\n"
    if http_checks:
        for h in http_checks:
            md += f"### {h.get('url', 'Unknown')}\n\n"
            md += f"**Status:** {h.get('status', h.get('error', 'Unknown'))}\n\n"
            if h.get('headers'):
                md += f"```\n{h['headers']}\n```\n\n"
    
    md += f"\n---\n\n*Report generated by RedSentinel*\n"
    return md


def export_report(target, data, format="html", output_dir="."):
    """Export report to file"""
    if format == "html":
        content = render_report(
            target, 
            data.get("subdomains"),
            data.get("ports"),
            data.get("http_checks")
        )
        ext = "html"
    elif format == "json":
        content = render_json_report(target, data)
        ext = "json"
    elif format == "markdown":
        content = render_markdown_report(
            target,
            data.get("subdomains"),
            data.get("ports"),
            data.get("http_checks")
        )
        ext = "md"
    else:
        raise ValueError(f"Unsupported format: {format}")
    
    filename = f"report_{target}_{now_iso().replace(':', '-')}.{ext}"
    filepath = os.path.join(output_dir, filename)
    
    with open(filepath, "w", encoding="utf-8") as f:
        f.write(content)
    
    return filepath
